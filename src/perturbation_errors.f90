  subroutine initialize_error_logger(log_filename)
    character(len=*), intent(in), optional :: log_filename

    perturbation_log%enabled = .true.
    perturbation_log%num_errors = 0

    if (present(log_filename)) then
      perturbation_log%log_file = log_filename
    else
      perturbation_log%log_file = 'perturbation_errors.log'
    end if

    if (allocated(perturbation_log%error_history)) then
      deallocate(perturbation_log%error_history)
    end if

  end subroutine initialize_error_logger

  !===========================================================================
  ! Log an error
  !===========================================================================
  subroutine log_error(error_info)
    type(perturbation_error), intent(in) :: error_info

    integer :: new_size
    type(perturbation_error), allocatable :: temp_history(:)

    if (.not. perturbation_log%enabled) return

    ! Expand error history array
    new_size = perturbation_log%num_errors + 1
    allocate(temp_history(new_size))

    if (perturbation_log%num_errors > 0) then
      temp_history(1:perturbation_log%num_errors) = perturbation_log%error_history
      deallocate(perturbation_log%error_history)
    end if

    temp_history(new_size) = error_info
    call move_alloc(temp_history, perturbation_log%error_history)
    perturbation_log%num_errors = new_size

    ! Print error to console
    call print_error_to_console(error_info)

  end subroutine log_error

  !===========================================================================
  ! Create an error object
  !===========================================================================
  function create_error(error_code, message, context, severity, can_recover, &
                        recovery_action) result(error_info)
    integer, intent(in) :: error_code
    character(len=*), intent(in) :: message
    character(len=*), intent(in), optional :: context
    integer, intent(in), optional :: severity
    logical, intent(in), optional :: can_recover
    character(len=*), intent(in), optional :: recovery_action
    type(perturbation_error) :: error_info

    error_info%error_code = error_code
    error_info%message = message
    error_info%severity = SEVERITY_ERROR
    error_info%can_recover = .true.

    if (present(context)) error_info%context = context
    if (present(severity)) error_info%severity = severity
    if (present(can_recover)) error_info%can_recover = can_recover
    if (present(recovery_action)) error_info%recovery_action = recovery_action

  end function create_error

  !===========================================================================
  ! Handle an error with automatic recovery if possible
  !===========================================================================
  subroutine handle_error(error_info, success)
    type(perturbation_error), intent(inout) :: error_info
    logical, intent(out) :: success

    success = .false.

    ! Log the error
    call log_error(error_info)

    ! Attempt recovery if possible
    if (error_info%can_recover) then
      select case (error_info%error_code)
        case (ERROR_STEP_SIZE_OUT_OF_RANGE)
          call recover_step_size_error(error_info, success)
        case (ERROR_NON_CONVERGENCE)
          call recover_convergence_error(error_info, success)
        case (ERROR_MEMORY_ALLOCATION)
          call recover_memory_error(error_info, success)
        case (ERROR_WORKSPACE_INSUFFICIENT)
          call recover_workspace_error(error_info, success)
        case default
          success = .false.
      end select
    end if

  end subroutine handle_error

