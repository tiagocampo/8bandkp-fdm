  !===========================================================================
  ! Print error summary
  !===========================================================================
  subroutine print_error_summary()
    integer :: i
    character(len=20) :: severity_str

    if (perturbation_log%num_errors == 0) then
      print *, "No errors logged."
      return
    end if

    print *, "=== Perturbation Error Summary ==="
    print *, "Total errors:", perturbation_log%num_errors

    do i = 1, perturbation_log%num_errors
      select case (perturbation_log%error_history(i)%severity)
        case (SEVERITY_INFO)
          severity_str = "INFO"
        case (SEVERITY_WARNING)
          severity_str = "WARNING"
        case (SEVERITY_ERROR)
          severity_str = "ERROR"
        case (SEVERITY_FATAL)
          severity_str = "FATAL"
        case default
          severity_str = "UNKNOWN"
      end select

      print *, i, ": [", trim(severity_str), "] ", &
               trim(perturbation_log%error_history(i)%message)
      if (len_trim(perturbation_log%error_history(i)%context) > 0) then
        print *, "    Context: ", trim(perturbation_log%error_history(i)%context)
      end if
    end do

  end subroutine print_error_summary

