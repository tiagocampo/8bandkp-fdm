        message = "Success: No error occurred"
      case (ERROR_INVALID_CONFIG)
        message = "Invalid configuration parameters"
      case (ERROR_INVALID_TARGET_STATE)
        message = "Invalid target state index"
      case (ERROR_INVALID_FIELD)
