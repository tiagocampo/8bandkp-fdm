      case (ERROR_NUMERICAL_DIVERGENCE)
        message = "Numerical divergence detected"
      case (ERROR_EIGENVALUE_FAILURE)
        message = "Eigenvalue solver failure"
      case (ERROR_NON_CONVERGENCE)
        message = "Calculation failed to converge"
      case (ERROR_MEMORY_ALLOCATION)
        message = "Memory allocation failure"
      case (ERROR_VALIDATION_FAILURE)
        message = "Validation test failure"
      case (ERROR_HAMILTONIAN_CONSTRUCTION)
        message = "Hamiltonian construction error"
      case (ERROR_STEP_SIZE_OUT_OF_RANGE)
        message = "Perturbation step size out of valid range"
      case (ERROR_DEGENERATE_STATES)
        message = "Highly degenerate states detected"
      case (ERROR_NON_HERMITIAN)
        message = "Non-Hermitian Hamiltonian detected"
      case (ERROR_SPARSE_SOLVER)
        message = "Sparse eigenvalue solver error"
      case (ERROR_WORKSPACE_INSUFFICIENT)
        message = "Insufficient workspace memory"
      case default
        message = "Unknown error occurred"
    end select

  end function get_error_message

